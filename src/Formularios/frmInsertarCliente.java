    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean a = false;
        if (jRadioButton1.isSelected() || jRadioButton2.isSelected()) {
            a = true;
        }
        if (!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("") && !jTextField4.getText().equals("") && !jTextField6.getText().equals("") && !jTextField7.getText().equals("") && !jTextField8.getText().equals("") && a == true) {
            int Id = Integer.parseInt(jTextField1.getText());
            String nom = jTextField2.getText();
            String ape = jTextField3.getText();
            int edad = Integer.parseInt(jTextField4.getText());
            String sex = "";
            if (jRadioButton1.isSelected()) {
                sex = "M";
            }
            if (jRadioButton2.isSelected()) {
                sex = "F";
            }
            String pro = jTextField6.getText();
            String ciu = jTextField7.getText();
            String corr = jTextField8.getText();
            ClienteDAO dao = new ClienteDAO();
            Cliente cli = new Cliente(Id, nom, ape, edad, sex, pro, ciu, corr);
            if (funcion.equals("Insertar")) {
                dao.InsertarCliente(cli);
                JOptionPane.showMessageDialog(null, "Cliente insertado existosamente", "Cliente creado", JOptionPane.INFORMATION_MESSAGE);
            }
            if (funcion.equals("Modificar")) {
                dao.ModificarCliente(cli, aux);
                JOptionPane.showMessageDialog(null, "Datos modificados existosamente", "Cliente modificado", JOptionPane.INFORMATION_MESSAGE);
            }
            frmAdminClientes.ListarClientes();
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingrese correctamente los datos","Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

