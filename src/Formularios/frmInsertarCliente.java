    int aux = 0;
    public void SetDatos(Cliente cli){
        jTextField1.setText("0"+String.valueOf(cli.getIdCliente()));
        aux = cli.getIdCliente();
        jTextField1.setEditable(true);
        jTextField2.setText(cli.getNombre());
        jTextField3.setText(cli.getApellido());
        jTextField4.setText(String.valueOf(cli.getEdad()));
        if(cli.getSexo().equals("M")){
            jRadioButton1.setSelected(true);
        }
        if(cli.getSexo().equals("F")){
            jRadioButton2.setSelected(true);
        }
        jTextField6.setText(cli.getProvincia());
        jTextField7.setText(cli.getCiudad());
        jTextField8.setText(cli.getCorreo());
   }
    
    public void GenerarSerieEmpleados(){
        ClienteDAO dao = new ClienteDAO();
        int serie = dao.NumeroSerie();
        if(serie==0){
            jTextField1.setText(String.valueOf(1));
        }
        else{
            int inc = serie+1;
            jTextField1.setText("0"+String.valueOf(inc));
            jTextField1.setEditable(false);
        }
    }
    
