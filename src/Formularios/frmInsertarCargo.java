    public void GenerarSerieEmpleados(){
        CargoDAO dao = new CargoDAO();
        int serie = dao.NumeroSerie();
        if(serie==0){
            jTextField1.setText(String.valueOf(1));
        }
        else{
            int inc = serie+1;
            jTextField1.setText(String.valueOf(inc));
            jTextField1.setEditable(false);
        }
    }
    
    public void SetDatos(Cargo car){
        jTextField1.setText(String.valueOf(car.getIdCargo()));
        jTextField2.setText(car.getNombre());
        jTextField3.setText(String.valueOf(car.getSueldo()));
        jTextField4.setText(car.getDescripcion());
    }
 
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("") && !jTextField4.getText().equals("")) {
            int id = Integer.parseInt(jTextField1.getText());
            String nombre = jTextField2.getText();
            String des = jTextField4.getText();
            double suel = Double.parseDouble(jTextField3.getText());
            Cargo car = new Cargo(id, nombre, des, suel);
            CargoDAO dao = new CargoDAO();
            if (funcion.equals("Insertar")) {
                dao.InsertarCargo(car);
                JOptionPane.showMessageDialog(null, "Cargo creado existosamente","Cargo creado",JOptionPane.INFORMATION_MESSAGE);
            }
            if (funcion.equals("Modificar")) {
                dao.ModificarCargo(car);
                JOptionPane.showMessageDialog(null, "Cargo modificado existosamente","Cargo modificado",JOptionPane.INFORMATION_MESSAGE);
            }
            frmAdminCargo.ListarDatos();
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingrese correctamente los datos","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

